          <a name="gapanalysis"></a>
          <h4>Gap Analysis</h4>
          <br />
          <p>
            The "Gap Analysis" is a detailed assessment of the existing system and business processes currently in
            operation. This assessment is performed by Pragmatics' qualified Lead Auditors, who in some cases perform 3rd
            party assessments for some of North Americas' largest Registrars.
          </p>

          <p>
            The assessment involves interviewing employees and management, recording existing practices and documentation
            and comparing these with the ISO 9001 requirements.
          </p>

          <p>
            The output is a comprehensive report which indicates areas of conformance, good practices and ultimately
            deficiencies in the system compared with ISO 9001.
          </p>

          <p>
            The report also provides specific guidance and recommendations to guide organizations in establishing the
            necessary "fixes" in order to move towards conformance.
          </p>

          <p>
            The report is not generic in nature and will contain examples and guidance specific to the clients' system and
            methods of operation.
          </p>

